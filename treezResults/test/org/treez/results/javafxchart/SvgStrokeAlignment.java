package org.treez.results.javafxchart;

/**
 * Defines how strokes are drown in respect to the shape, see
 * http://stackoverflow.com/questions/7241393/can-you-control-how-an-svgs-stroke-width-is-drawn
 */
public enum SvgStrokeAlignment {

	//#region VALUES

	/**
	 * The stroke is centered on the edge of the shape
	 */
	CENTER,

	/**
	 * The stroke is drawn inside the shape
	 */
	INNER,

	/**
	 * The stroke is drawn outside the shape
	 */
	OUTER,

	//#end region

	//#region ATTRIBUTES

	//#end region

	//#region CONSTRUCTORS

	//#end region
}
